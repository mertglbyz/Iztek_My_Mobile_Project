export const getMockApproachingBuses = (stopId: string): ApproachingBus[] => {
    // Her durağa göre farklı ama tutarlı mock veri
    const seed = parseInt(stopId, 10);

    const stop = MOCK_STOPS.find((s) => s.id === stopId);
    if (!stop) return [];

    return stop.routes.slice(0, 3).map((route, index) => ({
        id: `${stopId}-${index}`,
        routeNumber: route,
        destination: getDestinationForRoute(route),
        estimatedMinutes: (seed + index * 3) % 20 + 1,
        isAccessible: index % 2 === 0,
        isLowFloor: index === 0,
    }));
};

