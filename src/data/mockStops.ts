export const getMockApproachingBuses = (stopId: number): ApproachingBus[] => {
    const stop = MOCK_STOPS.find((s) => s.id === stopId);
    if (!stop) return [];

    const seed = stopId % 20;

    return stop.routes.slice(0, 3).map((route, index) => ({
        id: `${stopId}-${index}`,
        routeNumber: route,
        destination: getDestinationForRoute(route),
        estimatedMinutes: (seed + index * 3) % 20 + 1,
        isAccessible: index % 2 === 0,
        isLowFloor: index === 0,
    }));
};

